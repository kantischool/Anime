    @Json(name = "rating") val rating: String? = null,
    @Json(name = "score") val score: Double? = null,
    @Json(name = "scored_by") val scoredBy: Long? = null,
    @Json(name = "rank") val rank: Int? = null,
    @Json(name = "popularity") val popularity: Long? = null,
    @Json(name = "members") val members: Long? = null,
    @Json(name = "favorites") val favorites: Long? = null,
    @Json(name = "synopsis") val synopsis: String? = null,
    @Json(name = "background") val background: String? = null,
    @Json(name = "season") val season: String? = null,
    @Json(name = "year") val year: Int? = null,
    @Json(name = "broadcast") val broadcast: Broadcast? = null,
    @Json(name = "producers") val producers: List<Data>? = null,
    @Json(name = "licensors") val licensors: List<Data>? = null,
    @Json(name = "studios") val studios: List<Data>? = null,
    @Json(name = "genres") val genres: List<Data>? = null,
    @Json(name = "explicit_genres") val explicitGenres: List<Data>? = null,
    @Json(name = "themes") val themes: List<Data>? = null,
    @Json(name = "demographics") val demographics: List<Data>? = null,
)
