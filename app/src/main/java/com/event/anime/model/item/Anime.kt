package com.event.anime.model.item

import com.squareup.moshi.Json

data class Anime(
    @Json(name = "mal_id") val malId: Int? = null,
    @Json(name = "url") val url: String? = null,
    @Json(name = "images") val images: Images? = null,
    @Json(name = "trailer") val trailer: Trailer? = null,
    @Json(name = "approved") val approved: Boolean? = null,
    @Json(name = "titles") val titles: List<Title>? = null,
    @Json(name = "title") val title: String? = null,
    @Json(name = "title_english") val titleEnglish: String? = null,
    @Json(name = "title_japanese") val titleJapanese: String? = null,
    @Json(name = "title_synonyms") val titleSynonyms: List<String>? = null,
    @Json(name = "type") val type: String? = null,
    @Json(name = "source") val source: String? = null,
    @Json(name = "episodes") val episodes: Int? = null,
    @Json(name = "status") val status: String? = null,
    @Json(name = "airing") val airing: Boolean? = null,
    @Json(name = "aired") val aired: Aired? = null,
